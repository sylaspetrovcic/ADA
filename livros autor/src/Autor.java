public class Autor {
    String nacionalidade;
    String nome;

    Integer idade;
    Livro[] livros;

    public Autor(String nacionalidade, String nome,  Integer idade, Livro[] livros) {
        this.nacionalidade = nacionalidade;
        this.nome = nome;
        this.livros = livros;
        this.idade = idade;
        this.livros = livros;
    }
}
